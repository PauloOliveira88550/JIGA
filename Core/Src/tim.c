void SysTick_CallBack(void)
{
    TRIG_Ticks++;
    if(TRIG_Ticks >= 15) // Each 15msec
    {
        HCSR04_Trigger(HCSR04_SENSOR1);
        TRIG_Ticks = 0;
    }
}

void HAL_TIM_PeriodElapsedCallback( TIM_HandleTypeDef * htim ){


	if(htim->Instance == TIM2){

		HCSR04_TMR_OVF_ISR(htim);

	}


	if(htim->Instance == TIM3){

		sensor[0]=HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_8);

		sensor[1]=HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_9);

		sensor[2]=HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_10);

		sensor[3]=HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_11);

		sensor[4]=HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_12);

		for(i=0; i<5; i++)
			sensor[i]^=0x01;

		if((sensor[0]+sensor[1]+sensor[2]+sensor[3]+sensor[4])>0)
			y = (0 * sensor[0] + 1000 * sensor[1] + 2000 * sensor[2] + 3000 * sensor[3] + 4000 * sensor[4]) / (sensor[0] + sensor[1] + sensor[2] + sensor[3] + sensor[4]);

		else
			y = 2000;

		e = 2000-y;


		if(aut == 1){
			sum_e_bkp=sum_e;
			sum_e=sum_e+e_ant;
			u_d=Kd_h*(y-y_ant)+ad*u_d_ant;
			u=Kp_h*e+Ki_h*sum_e-u_d;
			e_ant=e;
			y_ant=y;
			u_d_ant=u_d;

			if (u>U_sat_a)
			{
				u=U_sat_a;
				sum_e=sum_e_bkp;
			}
			else if (u<U_sat_b)
			{
				u=U_sat_b;
				sum_e=sum_e_bkp;
			}

			motor_control(BaseVoltage - u, BaseVoltage + u);

		}
		else{
			e_ant=e;
			y_ant=y;
		}
	}
}


void motor_control(float l_voltage, float r_voltage){

	float pwm_l = 0;
	float pwm_r = 0;

	pwm_l = (l_voltage/MV)*100;
	pwm_r = (r_voltage/MV)*100;



	if(l_voltage >= 0){
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (abs(pwm_l) * 53999)/100);
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0);
	}
	if(l_voltage < 0){
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, (abs(pwm_l) * 53999)/100);
	}
	if(r_voltage >= 0){
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, 0);
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, (abs(pwm_r) * 53999)/100);
	}
	if(r_voltage < 0){
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, (abs(pwm_l) * 53999)/100);
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 0);
	}


}
